package org.qafellas.functions;

public class ArithmeticFunctions {
public int addition(int x, int y){
    return x+y;
}
public int subtraction(int x, int y){
    return x-y;
}
public int multiply(int x, int y){
    return x*y;
}
    public double divide(double x, double y){
        return x/y;
    }
}
